## N번째 큰 수

import heapq
import sys
input = sys.stdin.readline

n = int(input())
num = []

for i in range(n):
    for j in map(int, input().split()):
        if len(num) >= n:
            heapq.heappushpop(num, j)
        else:
            heapq.heappush(num, j)
        # print(num)

print(num[0])

